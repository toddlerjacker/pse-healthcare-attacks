np.random.seed(42)
df[["lat_j", "lon_j"]] = df.apply(
    lambda r: pd.Series(jitter_within(r["Latitude"], r["Longitude"], pse)),
    axis=1
)

fallbacks = ((df["lat_j"] == df["Latitude"]) & (df["lon_j"] == df["Longitude"])).sum()
