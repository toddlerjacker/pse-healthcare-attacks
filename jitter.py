def jitter_within(lat, lon, polygon, max_tries=300, scale=0.12):
    for _ in range(max_tries):
        jlat = lat + np.random.uniform(-scale, scale)
        jlon = lon + np.random.uniform(-scale, scale)
        if polygon.contains(Point(jlon, jlat)):
            return jlat, jlon
    return lat, lon  # fallback


bad_precision = ["(4) Province, State, Governorate", "(6) Country", "censored"]
